                        <p>
                            Here at Arogin Stroke Care Unit, we manage the stroke patient’s treatment by a specialist multidisciplinary team led by Stroke and Medical Consultants, a team of Physiotherapists. Arogin care home  recognises the importance of returning to their own home as quickly as possible.  The care & support that are necessary for the recovery. Stroke care team  offers an extensive range of services,  personalised treatment programmes and all the clinical and emotional support.
                        </p>

                        <p>
                            A multidisciplinary Stroke Care Team led by Consultant Neurologists and Physicians, experienced and qualified physiotherapists

                        </p>

                        <h5>What kind of Care will the post-stroke patients receive at arogin?</h5>

                        <p>
                            To deliver the highest quality care, our team has full access to all the facilities at arogin care home, from well facilitated physiotherapy hall to homely environment with  friendly care givers and skilled nurses  that enable us to deliver the highest quality care psychologically , emotionally and physically in both way, which patients will need following a Stroke.
                        </p>

                        <h5>Different level of assessment is done to identify the care need of the patient like:</h5>
                            <ul>
                                <li>1st level -a Consultant Neurologist’s assessment & guide at hospital</li>
                                <li>2nd level- A Consultant Physician to identify the medical care required.</li>
                                <li>3rd level - A physiotherapist assessment and prepare a treatment plan.</li>
                                <li>4th level- Nursing assessment & entire care plan will be developed after all the above assessment.</li>
                            </ul>

                        <p>By relearning skills they have lost, learning new skills and therapies & finding other ways to manage any permanent disabilities, Our Team helps people to regain as much independence as possible.</p>

                        <h5> During the care process arogin care home  we will provide:</h5>

                        <ul>
                            <li>Consultant Physicians on a regular basis.</li>
                            <li>24/7 skilled Nurses round the clock & close monitoring of the patients</li>
                            <li>24/7 caregiver & no attendees required</li>
                            <li>Radiology, Physiotherapy, Respiratory Physiology with the coordination of Consultant neurologist</li>
                            <li>High Dependency/Intensive Therapy Unit.</li>
                            <li>Critical care management team available at any time.</li>
                            <li>Lab & Pharmacy Services</li>
                        </ul>

                        <p>We truly understand  that getting back to their own home is of utmost importance and our service strives to motivate patients to participate on their own. 
                            We offer all the clinical and emotional support necessary to get the patient back to their own surroundings as quickly as possible,
                        </p>

                        <h5>Stroke special care includes:</h5>

                        <ul>
                            <li>Physiotherapy services</li>
                            <li>Speech & Language Therapy</li>
                            <li>Dieticians</li>
                        </ul>

                        <p>
                            Strokes can affect a large variety of functions, our Stroke Care Team can bring a Consultant from any discipline into the care package when required including Ophthalmologists (eye), Urologists (kidneys and urinary function) and Cardiologists (heart) .

                        </p>

                        <h5>Rehabilitation Services</h5>

                        <p>Our specialist team works with you to assist with your recovery from surgery or from a large range of neurological conditions. Evidence shows that Physical and occupational physiotherapy is effective across a range of neurological conditions and for  injuries such as stroke (or mini stroke), multiple sclerosis, brain injury and tumors, Parkinson’s disease, falls and damage to the nervous system</p>

                        <p>The strategy for rehabilitation involves a detailed assessment of the patient which is followed by an evaluation of their needs liket a return to work and exercise or to preserve the ability to perform daily activities such as dressing, cooking, cleaning and other daily activities. Our Rehabilitation physiotherapy team can help strengthen and mobilise muscles and joints to preserve existing function and help to explore new strategies to adopt ways of performing tasks</p>

                        <p>Our Arogin includes our experienced, expert and friendly team that welcomes each patient with a personalised care package including daily Consultant visits, blood tests, physiotherapy and a 24/7 nursing care service, we support their family members by providing reliable & trained caregivers where  attendees/are not required  to stay with patient</p>
