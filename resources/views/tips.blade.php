                        <p>
                            When there is a haemorrhage due to a burst blood vessel & When the blood supply to part of the brain is interrupted by a blockage in the blood supply and that is stroke.
                        </p>

                        <p>
                            If a patient has suffered or is suffering a stroke, the patient may experience weakness or paralysis in an arm and or leg,they may find  difficulty in speaking, they may lose their vision in part of the visual field or complain of numbness and / or tingling down one side of the body. It is always imperative to seek urgent medical advice if any of these events occur.
